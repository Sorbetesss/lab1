namespace Symfony\Component\Routing;

final class Alias
{
    /**
     * @var string
     */
    private $name;

    public function __construct(string $name)
    {
        $this->name = $name;
    }

    /**
     * Returns the name of this alias.
     *
     * @return string The alias name
     */
    public function __toString()
    {
        return $this->name;
    }
}
