namespace Symfony\Component\Mailer\Transport;

abstract class AbstractTransportSetting implements TransportSettingInterface
{
    private $name;
    private $value;

    public function __construct(string $name, $value)
    {
        $this->name = $name;
        $this->value = $value;
    }

    public function getName(): string
    {
        return $this->name;
    }

    /**
     * @return string|array
     */
    public function getValue()
    {
        return $this->value;
    }
}
