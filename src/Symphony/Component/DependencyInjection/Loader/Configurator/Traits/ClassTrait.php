namespace Symphony\Component\DependencyInjection\Loader\Configurator\Traits;

trait ClassTrait
{
    /**
     * Sets the service class.
     *
     * @return $this
     */
    final public function class($class)
    {
        $this->definition->setClass($class);

        return $this;
    }
}
